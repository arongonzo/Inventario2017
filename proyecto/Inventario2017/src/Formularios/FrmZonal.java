    private boolean validar_formulario()
    {
        boolean validar = false;
        
        if(txt_zonal.getText().trim()=="" || txt_zonal.getText().trim()=="" )
        {
            JOptionPane.showMessageDialog(null, "Todos los campo en Negrita son de acesso obligatorio");
            return false;
        } 
        else 
        {
            validar = true;
        }
        return validar;
    }
    
    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed

        if(validar_formulario()){

            boolean resp = false;
            Zonal znl;

            int id_zonal = Integer.parseInt(lblLlavezonal.getText());

            if(id_zonal ==0){
                znl = new Zonal(id_zonal, txt_zonal.getText(), txt_descripcion.getText());
                resp = zonales.AgregarZonal(znl);
            }
            else
            {
                znl = new Zonal(id_zonal, txt_zonal.getText(), txt_descripcion.getText());
                resp = zonales.UpdateZonal(znl);
            }

            if (resp == false) {
                JOptionPane.showMessageDialog(null, "Dato Agregado");
                ListarTabla();
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Dato no Agregado");
            }
        }
    }//GEN-LAST:event_btn_grabarActionPerformed

