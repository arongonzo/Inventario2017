    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed

        if(validar_formulario()){
            boolean resp = false;
            Unidad und;

            int id_unidad = Integer.parseInt(lblLlaveUnidad.getText());
            CmbZonal items = (CmbZonal)cmb_Zona.getSelectedItem();
            int id_zonal = Integer.parseInt(items.getID());
            
            if(id_unidad ==0){
                und = new Unidad(id_unidad, id_zonal, txt_unidad.getText());
                resp = unidades.AgregarUnidad(und);
            }
            else
            {
                und = new Unidad(id_unidad, id_zonal, txt_unidad.getText());
                resp = unidades.UpdateUnidad(und);
            }

            if (resp == false) {
                JOptionPane.showMessageDialog(null, "Dato Agregado");
                ListarTabla();
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Dato no Agregado");
            }
        }
    }//GEN-LAST:event_btn_grabarActionPerformed

