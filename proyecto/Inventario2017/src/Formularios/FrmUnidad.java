    private void llenarexcel()
    {
        XSSFWorkbook wb = new XSSFWorkbook();
        XSSFSheet ws = wb.createSheet();

        //load data to treemap
        TreeMap<String,Object[]> data = new TreeMap<>();
        int pos = 0;
        data.put("0",new Object[]{mtu.getColumnName(0), mtu.getColumnName(1)});

        for (Unidad usr : mtu.unidades) {
            data.put(String.valueOf(pos + 1),new Object[]{mtu.getValueAt(pos,0), mtu.getValueAt(pos,1)});
            pos++;
        }

        Set<String> ids = data.keySet();
        XSSFRow row;
        int rowId=0;

        for(String key:ids)
        {
            row=ws.createRow(rowId++);
            Object[] values = data.get(key);

            int cellId =0;
            for(Object o : values)
            {
                Cell cell = row.createCell(cellId++);
                cell.setCellValue(o.toString());
            }
        }
        try
        {
            FileOutputStream fos= new FileOutputStream(new File("c:/excel/exwcel.xlsx"));
            wb.write(fos);
            fos.close();
            JOptionPane.showMessageDialog(null, "Datos exportados");
        } 
        catch(FileNotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error en " + ex);

        } 
        catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error en " + ex);
        }
    }
    
